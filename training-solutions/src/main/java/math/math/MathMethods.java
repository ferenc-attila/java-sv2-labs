package math.math;

public class MathMethods {

    public static void main(String[] args) {

        System.out.println(Math.max(5,10));
        System.out.println(Math.min(5,10));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(45.52369));
        System.out.println(Math.abs(-45.7854));
        System.out.println(Math.negateExact(78));
        System.out.println(Math.addExact(10,15));
        System.out.println(Math.subtractExact(10,15));
        System.out.println(Math.multiplyExact(5,4));
        System.out.println(Math.pow(1.5,0.75));
        System.out.println(Math.incrementExact(1256));
        System.out.println(Math.decrementExact(1256));
        System.out.println(Math.random());
    }
}
