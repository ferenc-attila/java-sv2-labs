package sorting;

public class Car {

    private String brand;
    private int year;
    private int price;

    public Car(String brand, int year, int price) {
        this.brand = brand;
        this.year = year;
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public int getYear() {
        return year;
    }

    public int getPrice() {
        return price;
    }
}
